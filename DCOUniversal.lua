local function WXZVKNW_fake_script() -- Credits.CreditsScript 
	local script = Instance.new('LocalScript', Credits)

	on = false
	local creditresult = script.Parent.Parent.CIShow
	
	function show()
		creditresult.Visible = true
		repeat
			creditresult.BackgroundTransparency -= 0.05
			creditresult.TextTransparency -= 0.05
			task.wait()
		until math.round(creditresult.BackgroundTransparency) == 0
		creditresult.BackgroundTransparency = 0
		creditresult.TextTransparency = 0
	end
	
	function hide()
		creditresult.Visible = true
		repeat
			creditresult.BackgroundTransparency += 0.05
			creditresult.TextTransparency += 0.05
			task.wait()
		until math.round(creditresult.BackgroundTransparency) == 1
		creditresult.BackgroundTransparency = 1
		creditresult.TextTransparency = 1
	end
	
	script.Parent.MouseButton1Up:Connect(function()
		if on == false then
			on = true
			show()
		else
			on = false
			hide()
		end
	end)
end
